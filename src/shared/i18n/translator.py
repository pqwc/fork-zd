def _strip_mnemonics(text: str) -> str:
    """Убирает символы & (ускорители Qt), оставляя экранирование &&."""
    if not text or "&" not in text:
        return text
    out: list[str] = []
    i = 0
    while i < len(text):
        ch = text[i]
        if ch == "&" and i + 1 < len(text) and text[i + 1] == "&":
            out.append("&")
            i += 2
        elif ch == "&":
            i += 1
        else:
            out.append(ch)
            i += 1
    return "".join(out)


def tr(key: str, language: str = 'ru') -> str:
    """
    Получает перевод строки по ключу и языку
    
    Args:
        key: Ключ перевода
        language: Язык ('ru' или 'en')
    
    Returns:
        Переведенная строка или ключ, если перевод не найден
    """
    text = TRANSLATIONS.get(language, TRANSLATIONS['ru']).get(key, key)
    return _strip_mnemonics(text)


def tr_platform(base_key: str, language: str = 'ru') -> str:
    """Перевод с учётом платформы: ключ `{base}_linux` на Linux, иначе `base`."""
    from src.platform import is_linux

    if is_linux():
        linux_key = f"{base_key}_linux"
        catalog = TRANSLATIONS.get(language, TRANSLATIONS['ru'])
        if linux_key in catalog:
            return tr(linux_key, language)
    return tr(base_key, language)

